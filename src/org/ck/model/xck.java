package org.ck.model;

public class xck {

	int xckId;//小仓库id	
	double xckSize;//小仓库大小
	double xckHeight;//小仓库高度
	double xckSize1;//小仓库剩余容量
	public int getXckId() {
		return xckId;
	}
	public void setXckId(int xckId) {
		this.xckId = xckId;
	}
	public double getXckSize() {
		return xckSize;
	}
	public void setXckSize(double xckSize) {
		this.xckSize = xckSize;
	}
	public double getXckHeight() {
		return xckHeight;
	}
	public void setXckHeight(double xckHeight) {
		this.xckHeight = xckHeight;
	}
	public double getXckSize1() {
		return xckSize1;
	}
	public void setXckSize1(double xckSize1) {
		this.xckSize1 = xckSize1;
	}
	@Override
	public String toString() {
		return "xck [xckId=" + xckId + ", xckSize=" + xckSize + ", xckHeight="
				+ xckHeight + ", xckSize1=" + xckSize1 + "]";
	}
	
	
	
	
	
	
	
	
}
